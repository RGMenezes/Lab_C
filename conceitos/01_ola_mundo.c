#include <stdio.h>

int main(void)
{
    // A diretiva #include <stdio.h> importa recursos de entrada e saída padrão,
    // como a função printf, que usamos para mostrar mensagens na tela.

    // A função main é o ponto de entrada do programa em C.
    // Quando o programa inicia, a execução começa por aqui.

    printf("Olá, mundo! Bem-vindo à programação em C.\n");
    // Cada instrução em C normalmente termina com ponto e vírgula (;).
    // Esse símbolo indica ao compilador onde uma instrução termina.

    return 0;
    // O return 0 informa ao sistema operacional que o programa terminou
    // corretamente (sem erros de execução).
}
